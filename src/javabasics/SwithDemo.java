package javabasics;

public class SwithDemo {
    public static void main(String[] args) {
        int x =3;
        switch (x){
            case 1+1+1:
                System.out.println("Case 1");
                break;
            case 2:
                System.out.println("Case 2");
                break;
            default:
                System.out.println("Default");
        }
    }
}
